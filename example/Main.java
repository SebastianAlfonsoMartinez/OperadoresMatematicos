package org.example;

public class Main {
    public static void main(String[] args) {

        int num1 = 8;
        int num2 = 2;

        //Operadores matematicos
        System.out.println("Operadores matematicos");
        System.out.println(num1 + " + " + num2 + " = " + (num1 + num2));
        System.out.println(num1 + " - " + num2 + " = " + (num1 - num2));
        System.out.println(num1 + " * " + num2 + " = " + (num1 * num2));
        System.out.println(num1 + " / " + num2 + " = " + ((double)num1 / num2));
        System.out.println(num1 + " % " + num2 + " = " + (num1 % num2));

    }
}